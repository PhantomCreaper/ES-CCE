	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000 ; stack pointer value when stack is empty
	DCD Reset_Handler ; reset vector
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0, =SRC
	LDR R1, =SRC2
	LDR R10, =DST
	LDMIA R0, {R2-R5}
	LDMIA R1, {R6-R9}
	ADDS R5, R9
	ADC R4, R8
	ADC R3, R7
	ADC R2, R6
	STMIA R10, {R2-R5}
STOP
	B STOP
SRC DCD 0X11111111, 0X11111111, 0X11111111, 0X11111111
SRC2 DCD 0X22222222, 0X22222222, 0X22222222, 0X22222222
	AREA DATASEG, DATA, READWRITE
DST DCD 0X10000000
	END