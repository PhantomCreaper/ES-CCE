	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000 ; stack pointer value when stack is empty
	DCD Reset_Handler ; reset vector
	ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0, =SRC
	LDR R1, =SRC2
	LDR R10, =DST
	LDMIA R0, {R2-R5}
	LDMIA R1, {R6-R9}
	SUBS R5, R9
	SBC R4, R8
	SBC R3, R7
	SBC R2, R6
	STMIA R10, {R2-R5}
STOP
	B STOP
SRC DCD 0X99999999, 0X99999999, 0X99999999, 0X99999999
SRC2 DCD 0X55555555, 0X55555555, 0X55555555, 0X55555555
	AREA DATASEG, DATA, READWRITE
DST DCD 0X10000000
	END